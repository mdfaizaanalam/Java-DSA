public class InbuiltMethod {
    public static void main(String[] args) {

        System.out.println(Math.sqrt(64)); // square root of 64 = 8
        System.out.println(Math.pow(2,3)); // 2 to the power of 3
        System.out.println(Math.abs(-10)); // absolute value of -10 = 10
        System.out.println(Math.ceil(4.4)); // ceil value of 4.4 = 5
        System.out.println(Math.floor(4.4)); // floor value of 4.4 = 4
        System.out.println(Math.round(4.4)); // round value of 4.4 = 4
        System.out.println(Math.min(10, 20)); // min value of 10 and 20 = 10
        System.out.println(Math.max(10, 20)); // max value of 10 and 20 = 20
        System.out.println(Math.random()); // random value between 0 and 1
    }
}
